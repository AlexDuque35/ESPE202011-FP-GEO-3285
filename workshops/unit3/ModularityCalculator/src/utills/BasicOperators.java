public class BasicOperators {

    public static float addTwoNumbers(float number1, float number2) {
        float result = 0.0F;
        result = number1 + number2;
        return result;
    }

    public static float subtractTwoNumbers(float number1, float number2) {
        float result = 0.0F;
        result = number1 - number2;
        return result;
    }

    public static float multiplyTwoNumbers(float number1, float number2) {
        float result = 0.0F;
        result = number1 * number2;
        return result;
    }
    
     public static float divideTwoNumbers(float number1, float number2){
        float result = 0.0F;
        result = number1 / number2;
        return result;
    }
}
